SELECT c.COUNTRY_ID, COUNTRY_NAME, r.REGION_ID, r.REGION_NAME, LOCATION_ID, STREET_ADDRESS
	FROM REGIONS r
	LEFT JOIN COUNTRIES c 
	ON r.REGION_ID = c.REGION_ID
	LEFT JOIN LOCATIONS l ON c.COUNTRY_ID = c.COUNTRY_ID;

