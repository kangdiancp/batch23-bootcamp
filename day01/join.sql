	GROUP BY DEPARTEMENT_ID;

SELECT DEPARTEMENT_ID, SUM(SALARY) FROM EMPLOYEES
	GROUP BY DEPARTEMENT_ID
	HAVING SUM(SALARY) >= 6500;

SELECT DEPARTEMENT_ID, MIN(SALARY) FROM EMPLOYEES
	GROUP BY DEPARTEMENT_ID;

SELECT DEPARTEMENT_ID, MAX(SALARY) FROM EMPLOYEES
	GROUP BY DEPARTEMENT_ID;

SELECT DEPARTEMENT_ID, AVG(SALARY) FROM EMPLOYEES
	GROUP BY DEPARTEMENT_ID;
	
